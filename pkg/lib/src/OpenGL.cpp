namespace boom {

OpenGL::~OpenGL() {
    glDestroyContext(_context);
}

OpenGL::OpenGL(boom::OpenGLOptions const& options)
	: _context(nullptr)
{
    if (glLoaded() == false) {
        glLoad();
    }
    _context = glCreateContext({
        .majorVersion = options.majorVersion.value_or(3),
        .minorVersion = options.minorVersion.value_or(2)
    });
}

void OpenGL::clearColor(float red, float green, float blue, float alpha) {
    _makeCurrent();
    glClearColor(red, green, blue, alpha);
}

void OpenGL::clear(std::uint32_t mask) {
    _makeCurrent();
    glClear(mask);
}

void OpenGL::flush() {
    _makeCurrent();
    glFlush();
}

void OpenGL::swap() {
    _makeCurrent();
    glSwapBuffers(_context);
}

void OpenGL::_makeCurrent() {
    glMakeCurrent(_context);
}

} /* namespace boom */

